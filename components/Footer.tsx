const ICON_LABEL: Record<string, string> = {
  x: "X",
  linkedin: "LinkedIn",
  youtube: "YouTube",
  instagram: "Instagram",
  email: "Email",
};

export default function Footer() {
  const [days, setDays] = useState<number | null>(null);

  useEffect(() => {
    const birth = new Date(BIRTH_ISO).getTime();
    const update = () => setDays(Math.floor((Date.now() - birth) / 86400000));
    update();
    const id = setInterval(update, 60000);
    return () => clearInterval(id);
  }, []);

  return (
    <>
      <div className="eternity" aria-label="Days into eternity">
        {days !== null ? `✦ ${days.toLocaleString()} days into eternity` : ""}
      </div>

      <nav className="social" aria-label="Social links">
        {SOCIAL_LINKS.filter((s) => s.url && ICON_PATHS[s.platform]).map((s) => {
          const external = !s.url.startsWith("mailto:");
          return (
            <a
              key={s.platform}
              href={s.url}
              aria-label={ICON_LABEL[s.platform] ?? s.platform}
              {...(external ? { target: "_blank", rel: "noopener noreferrer" } : {})}
            >
              <svg viewBox="0 0 24 24" fill="currentColor">
                <path d={ICON_PATHS[s.platform]} />
              </svg>
            </a>
          );
        })}
      </nav>
    </>
  );
}
